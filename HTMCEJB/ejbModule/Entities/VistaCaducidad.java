package Entities;

import java.io.Serializable;
import javax.persistence.*;


/**
 * The persistent class for the vista_caducidad database table.
 * 
 */
@Entity
@Table(name="vista_caducidad")
@NamedQuery(name="VistaCaducidad.findAll", query="SELECT v FROM VistaCaducidad v")
public class VistaCaducidad implements Serializable {
	private static final long serialVersionUID = 1L;

	@Column(name="\"Apr\"")
	private String apr;

	@Column(name="\"Aug\"")
	private String aug;

	@Column(name="\"Dec\"")
	private String dec;

	private String descripcion;

	@Column(name="\"Feb\"")
	private String feb;

	private String itemfk;

	@Column(name="\"Jan\"")
	private String jan;

	@Column(name="\"Jul\"")
	private String jul;

	@Column(name="\"Jun\"")
	private String jun;	

	@Column(name="\"Mar\"")
	private String mar;

	@Column(name="\"May\"")
	private String may;

	@Column(name="\"Nov\"")
	private String nov;

	@Column(name="\"Oct\"")
	private String oct;

	@Id
	@Column(name="row_number")
	private Long rowNumber;

	@Column(name="\"Sep\"")
	private String sep;

	public VistaCaducidad() {
	}

	public String getApr() {
		return this.apr;
	}

	public void setApr(String apr) {
		this.apr = apr;
	}

	public String getAug() {
		return this.aug;
	}

	public void setAug(String aug) {
		this.aug = aug;
	}

	public String getDec() {
		return this.dec;
	}

	public void setDec(String dec) {
		this.dec = dec;
	}

	public String getDescripcion() {
		return this.descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getFeb() {
		return this.feb;
	}

	public void setFeb(String feb) {
		this.feb = feb;
	}

	public String getItemfk() {
		return this.itemfk;
	}

	public void setItemfk(String itemfk) {
		this.itemfk = itemfk;
	}

	public String getJan() {
		return this.jan;
	}

	public void setJan(String jan) {
		this.jan = jan;
	}

	public String getJul() {
		return this.jul;
	}

	public void setJul(String jul) {
		this.jul = jul;
	}

	public String getJun() {
		return this.jun;
	}

	public void setJun(String jun) {
		this.jun = jun;
	}
	

	public String getMar() {
		return this.mar;
	}

	public void setMar(String mar) {
		this.mar = mar;
	}

	public String getMay() {
		return this.may;
	}

	public void setMay(String may) {
		this.may = may;
	}

	public String getNov() {
		return this.nov;
	}

	public void setNov(String nov) {
		this.nov = nov;
	}

	public String getOct() {
		return this.oct;
	}

	public void setOct(String oct) {
		this.oct = oct;
	}

	public Long getRowNumber() {
		return this.rowNumber;
	}

	public void setRowNumber(Long rowNumber) {
		this.rowNumber = rowNumber;
	}

	public String getSep() {
		return this.sep;
	}

	public void setSep(String sep) {
		this.sep = sep;
	}

}